"use client";

import { cn } from "@/lib/utils";
import { cva, type VariantProps } from "class-variance-authority";
import { forwardRef, type ButtonHTMLAttributes } from "react";

const buttonVariants = cva(
  "inline-flex items-center justify-center rounded-md text-sm font-medium transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50",
  {
    variants: {
      variant: {
        default:
          "bg-[var(--accent-gold)] text-[var(--background)] hover:bg-[var(--accent-gold-hover)]",
        secondary:
          "bg-[var(--background-secondary)] text-[var(--foreground)] hover:bg-[var(--background-tertiary)] border border-[var(--border)]",
        outline:
          "border border-[var(--border)] bg-transparent hover:bg-[var(--background-secondary)] hover:border-[var(--border-hover)]",
        ghost:
          "hover:bg-[var(--background-secondary)] hover:text-[var(--foreground)]",
        destructive:
          "bg-[var(--accent-red)] text-white hover:bg-[var(--accent-red)]/90",
        link: "text-[var(--accent-gold)] underline-offset-4 hover:underline",
      },
      size: {
        default: "h-10 px-4 py-2",
        sm: "h-8 rounded-md px-3 text-xs",
        lg: "h-12 rounded-md px-8 text-base",
        icon: "h-10 w-10",
      },
    },
    defaultVariants: {
      variant: "default",
      size: "default",
    },
  }
);

export interface ButtonProps
  extends ButtonHTMLAttributes<HTMLButtonElement>,
    VariantProps<typeof buttonVariants> {}

const Button = forwardRef<HTMLButtonElement, ButtonProps>(
  ({ className, variant, size, ...props }, ref) => {
    return (
      <button
        className={cn(buttonVariants({ variant, size, className }))}
        ref={ref}
        {...props}
      />
    );
  }
);
Button.displayName = "Button";

export { Button, buttonVariants };
